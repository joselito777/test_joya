<p>
	Im Bereich der Perlen konzentrieren wir uns auf die südliche Hemisphäre, insbesondere auf Australien, Indonesien, Philippinen und Tahiti, wie auch auf die Süßwasserperlen der Flüsse und Seen Chinas. Diesen widmen wir unsere besondere Aufmerksamkeit und können hier mit einem besonders großen Angebots-Spektrum aufwarten.
</p>
<p>
	Unserer Liebe zu Perlen entspricht die ständige Suche nach Qualität, denn es kann keinesfalls um Perlen gehen, die schnell oder in großer Anzahl wachsen! So benötigt man zur Aufzucht im Mittel 18 bis 24 Monate, je nachdem ob es sich um eine erste oder zweite Züchtung handelt.
</p>
