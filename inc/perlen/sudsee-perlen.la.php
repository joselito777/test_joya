<p>
	Las perlas de los mares del sur son principalmente cultivadas en Australia, como también así en Filipinas, Indonesia y otras regiones indopacíficas, donde es probable contar diariamente con la anexión de nuevas granjas.
</p>
<p>
	En Australia, todavía se juntan las ostras a mano desde los fondos océanicos vecinos a la Eighty Mile Beach a mar abierto. La peligrosidad de estas aguas, hacen que sean los buzos las personas mejor pagas en la industria de la perla. La medusa conocida como“Box Jelly Fish” , los cocodrilos de agua salada, los tiburones, o las serpientes más venenosas del mundo se dan cita en estas latitudes. Las fuertes corrientes marinas constituyen uno de los mayores peligros con mareas de más de diez metros, a su vez aportan siempre oxígeno oceánico a sus aguas, donde las ostras usadas para el cultivo de la perla tienen condicionantes óptimos para crecer como ninguna ostra en el mundo lo hace.
</p>
<p>
	Las perlas australianas son las más valiosas en el mundo. El más caro de los collares vendidos alcanzó en la famosa casa de subastas Southeby una cifra mayor a los dos millones de dólares.
</p>
