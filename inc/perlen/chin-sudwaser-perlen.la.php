<p>
	Sin duda, no existen otras perlas que ofrezcan una natural gama de colores más extensos ni perlas cultivadas más ricas en perlamadre que las perlas de agua dulce de la China. Su cultivo se realiza principalmente en el Lago “Tai Hu” que es el tercer lago más grande de China, (2,5 veces mayor que el lago Lago de Konstanza, el mayor lago de Alemania) pero que en su parte más profunda no alcanza a 1,5 mts de profundidad lo que hace de este un lago prácticamente innavegable. Esta curiosidad se refleja en la falta de contaminación que protege el cultivo de la perla.
</p>
<p>
	Las perlas de cultivo de agua dulce en China son probablemente las más extendidas en el globo. La posibilidad de cultivar hasta 90 perlas en una ostra y el accesible costo de mano de obra, hacen de esta, la perla comercialmente más explotada.
</p>
<p>
	En China, como en el caso de la mayoría de las zonas de cultivo de perlas, para provocar la formacion de perlas se induce una parte central redonda.  Entre otras, se ha experimentado con formas de lenteja, redondas, ovaladas, de grano inflado (“Corn Flakes”).
</p>
<p>
	Con un trozo de epitelio hasta se pueden formar letras o perlas de casi todo tipo de capricho humano.
</p>
<p>
	También se ha coloreado con tinturas para un mercado masivo que no tiene mas exigencias que acompañar la moda.
No obstante, existe  también el cultivo de una calidad mayor a la norma y es esta, la que trabajamos en nuestro negocio.
</p>
<p>
Es	ta perla se consigue, tras varios anos de trabajo, y de técnicas conquistadas a través de muchos anos de experiencia y no es raro que –debido a su excelencia- los precios de estas, estén sobre los precios de las perlas de cultivo de Tahiti y hasta las de Australia.
</p>
