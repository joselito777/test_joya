<h3>Technische Ausbildung</h3>
<p>
	Nicolas Piaggio (geb.1954, 3 Kinder) ist Sachverständiger für Diamanten (D.D.I. – Deutsches Diamanteninstitut Pforzheim) und seit 1994 Spezialist im Bereich der Zuchtperlen und organischen Substanzen (Deutsche Gesellschaft für Gemmologie Idar-Oberstein).
</p>
<p>
	Zahlreiche Kurse in renommierten Fortbildungsinstituten, sowie Reisen als Journalist in die entlegensten Regionen unseres Planeten, zu Rubin-, Saphir- und Diamantenminen wie auch zu Zuchtperlen-Farmen belegen seine soliden technischen Kenntnisse. In seinem Heimatland Argentinien war er Student der Veterinärmedizin und Journalist. Er ist Autor verschiedener in Deutsch und Spanisch verfasster Beiträge aus dem Juwelier-Bereich. Alle Photos seiner Veröffentlichungen stammen von ihm. Hier seien genannt „Über Perlen“ (First), „Perlen“ (Natur und Kosmos), „Zuchtperlen aus China“ (Goldschmiedezeitung). Er trat als Vortragender vor verschiedenen Zuhörer-Kreisen auf, wie z.B. dem Lyons-Club und den Soroptimisten und lud auch zu Vorträgen in den eigenen Geschäftsräumen ein.
</p>
<p>
	Er hat in deutscher Sprache in eigenen Veröffentlichungen folgende Titel geschrieben:
	<li>Aus dem Feuer in das Licht, Göttingen 1998 </li>
	<li>Über die Diamantenmine in Kununurra-Australien, Göttingen 1998</li>
	<li>Streifzüge ins Licht, Göttingen 1999</li>
	<li>Streifzüge ins Licht (gleichnamiger Dokumentarfilm über Zuchtperlen in Australien und Polynesien), Göttingen 2000</li>
</p>
