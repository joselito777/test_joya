<h3>Formation technique</h3>
<p>
Nicolas Piaggio (1954 – 3 enfants) est expert en diamants (D.D.I. Fondation allemande de diamants Pforzheim) et spécialiste dans le domaine des perles de culture et des substances organiques (Société allemande de gemmologie – Idar Oberstein) depuis 1994.
</p>
<p>
De nombreux cours réalisés dans des instituts de formation professionnelle, des voyages en tant que journaliste dans des régions les plus retirées du monde, dans des mines de rubis, saphirs et diamants, ainsi que dans des fermes de perles sont à la base de sa solide connaissance technique. 
</p>
<p>
En Argentine, son pays natal, il a été journaliste et il a fait des études en sciences vétérinaires. Il a publié des articles en allemand et en espagnol au sujet de la joaillerie. La plupart de ses publications sont illustrées de ses propres photos : “Perles” (First), “Perlen” (Natur und Kosmos) et “Zuchtperlen aus China” (Goldschmiede Zeitung). Il a donné des conférences adressées à différents groupes et sociétés, parmi lesquels : Lyons Club, Soroptimist International, Rotary. Il est propriétaire et directeur général de cette bijouterie.
</p>
<p>
Il a publié en allemand les titres suivants :<br>
&bull; “Depuis le feu jusqu’à la lumière” (Aus dem feuer in das Licht), Göttingen, 1998.<br>
&bull; “À propos de la mine de diamants à Kununurra – Australia”, Göttingen, 1998.<br>
&bull; “Ballade dans la lumière” (Streifzüge ins Licht), Göttingen, 1999.<br>
&bull; Le tournage du documentaire du même nom a été fait dans des zones de culture de perles en Australie et dans la Polynésie française (Göttingen, 2000).
</p>
