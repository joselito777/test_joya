<h3>About us</h3>
<p>
For more than twenty years we have committed ourselves to the handcraft jewellery 
tradition. We have remained faithful to the principles that guided our work since our 
very beginning, pursuing something more than just economic profit.
</p>
<p>
Our never-ending search for both innovation and tradition in goldsmith’s techniques 
has led us along the path of a creative and experimental style.
</p>
<p>
Our work has focused on the combination of precious metals such us platinum, 
palladium and pure gold. These metals, due to their high melting point –higher 
than silver and gold’s (750-000 / 585-000 / 333-000)- represent a challenge to 
our work since they require special techniques and tools. Working with them is a 
personal demanding experience which aims at satisfying our customers’ desires and 
expectations.
</p>

<h3>The more you know about jewellery, the better your opinion about us.</h3>
<p>
In order to inform our customers about our field of interest, we have organized lectures 
and panels on pearls and diamonds in our atelier. 
</p>
<p>
The best-known German-speaking experts have visited our studio. Elisabeth Strack, 
Hans Schoeffel, Gaby Tolkowsky, Jeannette Fiedler, Karl Mayer Amler, Erhard 
Brepohl, Gregor Schwarz, and Nicolás Piaggio himself have given illuminating lectures 
on the subject.
</p>
