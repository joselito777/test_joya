<h3>Technical Training</h3>
<p>
Nicolás Piaggio, (b. 1954 - married - three children), is an expert in pearls and diamonds (DDI -Pforzheim German Diamonds Foundation), and an expertise on cultured pearls and organic substances (Idar Oberstein - German Gem Society since 1994).
</p>
<p>
In Argentina, his home Country, he was a journalist and a veterinarian student. The numerous seminars and courses he attended, as well as his travels to pearl farms and to diamond, ruby and sapphire mines all over the world, are the basis of his sound technical knowledge on the subject. He has given lectures in several institutions such as Lyons Club, Soroptimist International, and Rotary Club, among others.
</p>
<p>
Being an underwater-photographer and a journalist, he is now publishing in specialized magazines and journals both in Spanish and German, and is at the moment working on his own film productions. Most of his published articles -Pearls (First), Perlen (Natur und Kosmos) and Zuchtperlen aus China (Goldschmiede Zeitung)- have been illustrated with his own photographs.
</p>
<p>
He is the owner and manager of this jewellery store.
</p>
<p>
The followings are some of his published articles:<br>
&bull; "Out of the fire into the light", about the Argyle Diamond Mine in Kununurra/ Australia (Aus dem Feuer in das Licht-Göttingen, 1998, Göttingen 1998) and<br>
&bull; "Excursions into the light" about the culture of south-sea-pearls, (Streifzüge ins Licht - Göttingen 1999).<br>
</p>
<p>
He has also directed a documentary on pearl farms in Australia and the French Polynesia.
</p>
